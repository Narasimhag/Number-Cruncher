    private void timeStopperActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timeStopperActionPerformed
        // TODO add your handling code here:
        endTime = System.nanoTime();
        long timeDelay =  (long) ((endTime - startTime) * Math.pow(10, -9));
        timeDisplayer.setText(Long.toString(timeDelay));
        
    }//GEN-LAST:event_timeStopperActionPerformed

