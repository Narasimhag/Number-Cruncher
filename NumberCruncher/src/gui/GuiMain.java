    private void numGeneratorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_numGeneratorActionPerformed
        // TODO add your handling code here:
        RandNumGenerator r = new RandNumGenerator();
        if(r1.isSelected())        
        numberDisp.setText(Long.toString(r.showRandomInteger(10000, 99999)));
        else if(r2.isSelected())        
        numberDisp.setText(Long.toString(r.showRandomInteger(100000, 999999)));
        else if(r3.isSelected())        
        numberDisp.setText(Long.toString(r.showRandomInteger(1000000, 9999999)));
        else if(r4.isSelected())        
        numberDisp.setText(Long.toString(r.showRandomInteger(10000000, 99999999)));
        
        startTime = System.nanoTime();
        
    }//GEN-LAST:event_numGeneratorActionPerformed

